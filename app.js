function loadState() {
  const saved = readStorage();
  if (!saved) return initializeProfiles(structuredClone(defaultState));

  try {
    const parsed = JSON.parse(saved);
    if (Array.isArray(parsed.profiles) && parsed.profiles.length > 0) {
      const profiles = parsed.profiles.map(normalizeProfile);
      const activeProfileId = parsed.activeProfileId || profiles[0].id;
      const activeProfile = profiles.find((profile) => profile.id === activeProfileId) || profiles[0];
      return stateFromProfile(activeProfile, profiles, activeProfile.id);
    }

    if (!Array.isArray(parsed.holdings) || parsed.holdings.length === 0) {
      return initializeProfiles(structuredClone(defaultState));
    }

    const migrated = {
      ...structuredClone(defaultState),
      ...parsed,
      holdings: parsed.holdings.map((holding) => normalizeHoldingCurrency(holding, parsed.currency || defaultState.currency)),
      transactions: Array.isArray(parsed.transactions) ? parsed.transactions : [],
    };
    return initializeProfiles(migrated);
  } catch {
    return initializeProfiles(structuredClone(defaultState));
  }
}

function saveState() {
  syncActiveProfile();
  writeStorage(JSON.stringify(state));
}

function initializeProfiles(source) {
  const profile = normalizeProfile({
    ...getProfileData(source),
    id: source.activeProfileId || "default",
    name: source.profileName || defaultState.profileName,
    performanceHistory: source.performanceHistory || initialPerformanceHistory,
  });
  return stateFromProfile(profile, [profile], profile.id);
}

function normalizeProfile(profile) {
  return {
    id: profile.id || makeId(),
    name: profile.name || defaultState.profileName,
    cash: numberValue(profile.cash),
    threshold: numberValue(profile.threshold || defaultState.threshold),
    currency: profile.currency || defaultState.currency,
    fxRate: numberValue(profile.fxRate || defaultState.fxRate),
    priceRefreshInterval: numberValue(profile.priceRefreshInterval),
    priceProxyUrl: profile.priceProxyUrl || globalThis.PRICE_PROXY_URL || "",
    chartRange: profile.chartRange || "all",
    holdings: Array.isArray(profile.holdings) && profile.holdings.length > 0 ? profile.holdings.map((holding) => normalizeHoldingCurrency(holding, profile.currency)) : structuredClone(defaultState.holdings),
    transactions: Array.isArray(profile.transactions) ? profile.transactions : [],
    performanceHistory: Array.isArray(profile.performanceHistory) ? profile.performanceHistory : structuredClone(initialPerformanceHistory),
  };
}

function stateFromProfile(profile, profiles, activeProfileId) {
  return {
    ...structuredClone(defaultState),
    ...getProfileData(profile),
    profileName: profile.name,
    activeProfileId,
    profiles,
  };
}

function getProfileData(source) {
  return {
    cash: source.cash,
    threshold: source.threshold,
    currency: source.currency,
    fxRate: source.fxRate,
    priceRefreshInterval: source.priceRefreshInterval,
    priceProxyUrl: source.priceProxyUrl,
    chartRange: source.chartRange || "all",
    holdings: Array.isArray(source.holdings) ? source.holdings : [],
    transactions: Array.isArray(source.transactions) ? source.transactions : [],
    performanceHistory: Array.isArray(source.performanceHistory) ? source.performanceHistory : undefined,
  };
}

function syncActiveProfile() {
  if (!Array.isArray(state.profiles) || state.profiles.length === 0) return;

  const index = state.profiles.findIndex((profile) => profile.id === state.activeProfileId);
  if (index < 0) return;

  state.profiles[index] = normalizeProfile({
    ...getProfileData(state),
    id: state.activeProfileId,
    name: state.profileName,
    performanceHistory,
  });
}

function loadHistory() {
  if (Array.isArray(state.performanceHistory)) {
    return state.performanceHistory.filter((item) => item && item.date && Number.isFinite(Number(item.value)));
  }

  try {
    const saved = localStorage.getItem(historyKey);
    const parsed = saved ? JSON.parse(saved) : initialPerformanceHistory;
    const history = Array.isArray(parsed) ? parsed.filter((item) => item && item.date && Number.isFinite(Number(item.value))) : [];
    return history.length > 0 ? history : structuredClone(initialPerformanceHistory);
  } catch {
    return structuredClone(initialPerformanceHistory);
  }
}

function saveHistory() {
  syncActiveProfile();
  try {
    localStorage.setItem(historyKey, JSON.stringify(performanceHistory));
  } catch {
    // History is optional when the file is opened in stricter browsers.
  }
}

function readStorage() {
  try {
    const legacy = readLegacyStorage();
    const current = localStorage.getItem(storageKey);
    if (current) {
      return shouldPreferLegacyStorage(current, legacy) ? legacy : current;
    }

    return legacy;
  } catch {
    return null;
  }
}

function readLegacyStorage() {
  for (const key of ["rebalance-site-state-v8", "rebalance-site-state-v7", "rebalance-site-state-v6", "rebalance-site-state-v5"]) {
    const legacy = localStorage.getItem(key);
    if (legacy) return legacy;
  }
  return null;
}

function shouldPreferLegacyStorage(currentText, legacyText) {
  if (!legacyText) return false;

  try {
    const current = JSON.parse(currentText);
    const legacy = JSON.parse(legacyText);
    const currentProfile = Array.isArray(current.profiles) ? current.profiles[0] : current;
    const currentHoldings = Array.isArray(currentProfile?.holdings) ? currentProfile.holdings : [];
    const legacyHoldings = Array.isArray(legacy?.holdings) ? legacy.holdings : [];
    const currentSymbols = currentHoldings.map((holding) => holding.symbol).join("|");
    const defaultSymbols = defaultState.holdings.map((holding) => holding.symbol).join("|");
    const hasOnlyDefaultProfile = Array.isArray(current.profiles) && current.profiles.length === 1;
    const hasNoTransactions = !Array.isArray(currentProfile.transactions) || currentProfile.transactions.length === 0;

    return hasOnlyDefaultProfile && hasNoTransactions && currentSymbols === defaultSymbols && legacyHoldings.length > 0;
  } catch {
    return false;
  }
}

function writeStorage(value) {
  try {
    localStorage.setItem(storageKey, value);
  } catch {
    // The app still works as a standalone file if browser storage is blocked.
  }
}

function currencyFormatter() {
  return new Intl.NumberFormat("zh-TW", {
    style: "currency",
    currency: state.currency,
    maximumFractionDigits: state.currency === "JPY" ? 0 : 2,
  });
}

function twdFormatter() {
  return new Intl.NumberFormat("zh-TW", {
    style: "currency",
    currency: "TWD",
    maximumFractionDigits: 0,
  });
}

function numberValue(value) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : 0;
}

function isTwdQuotedSymbol(symbol) {
  return inferMarket(symbol) === "TW";
}

function normalizeHoldingCurrency(holding, fallbackCurrency = defaultState.currency) {
  const market = normalizeMarket(holding.market || inferMarket(holding.symbol));
  const quoteCurrency = quoteCurrencyForMarket(market, holding.quoteCurrency || fallbackCurrency);
  return { ...holding, market, quoteCurrency };
}

function normalizeMarket(market) {
  const value = String(market || "AUTO").trim().toUpperCase();
  return ["AUTO", "TW", "US", "UK"].includes(value) ? value : "AUTO";
}

function inferMarket(symbol) {
  const value = String(symbol ?? "").trim().toUpperCase();
  if (!value) return "AUTO";
  if (/^\d{4,6}$/.test(value) || /\.(TW|TWO)$/.test(value)) return "TW";
  if (/\.(L|UK)$/.test(value)) return "UK";
  if (["VWRA", "VWRP", "VWRL", "VUSA", "VUAG", "IMID", "AVGS", "JPGL", "CSPX", "IWDA", "SWDA", "EIMI", "ISAC", "IUIT"].includes(value)) return "UK";
  return "US";
}

function quoteCurrencyForMarket(market, fallbackCurrency = defaultState.currency) {
  if (market === "TW") return "TWD";
  if (market === "US" || market === "UK") return "USD";
  return fallbackCurrency || defaultState.currency;
}

function convertValueBetweenCurrencies(value, fromCurrency, toCurrency, fxRate) {
  const sourceCurrency = fromCurrency || defaultState.currency;
  const targetCurrency = toCurrency || defaultState.currency;
  const rate = Math.max(numberValue(fxRate), 0.0001);
  if (sourceCurrency === targetCurrency) return value;
  if (sourceCurrency === "TWD" && targetCurrency !== "TWD") return value / rate;
  if (sourceCurrency !== "TWD" && targetCurrency === "TWD") return value * rate;
  return value;
}

function marketLabel(market) {
  return {
    AUTO: "\u81ea\u52d5",
    TW: "\u53f0\u80a1",
    US: "\u7f8e\u80a1",
    UK: "\u82f1\u80a1",
  }[normalizeMarket(market)];
}

function convertToPortfolioCurrency(value, quoteCurrency) {
  return convertValueBetweenCurrencies(value, quoteCurrency, state.currency, state.fxRate);
}

function pct(value) {
  return `${value.toFixed(2)}%`;
}

function signedMoney(value, format) {
  const prefix = value > 0 ? "+" : "";
  return `${prefix}${format.format(value)}`;
}

function getPortfolio() {
  const holdings = state.holdings.map((rawHolding) => {
    const holding = normalizeHoldingCurrency(rawHolding, state.currency);
    const shares = numberValue(holding.shares);
    const avgCost = numberValue(holding.avgCost);
    const price = numberValue(holding.price);
    const target = numberValue(holding.target);
    const portfolioPrice = convertToPortfolioCurrency(price, holding.quoteCurrency);
    const portfolioAvgCost = convertToPortfolioCurrency(avgCost, holding.quoteCurrency);
    const marketValue = shares * portfolioPrice;
    const cost = shares * portfolioAvgCost;
    const profit = marketValue - cost;
    const returnRate = cost > 0 ? (profit / cost) * 100 : 0;

    return { ...holding, shares, avgCost, price, target, marketValue, cost, profit, returnRate };
  });

  const stockValue = holdings.reduce((sum, holding) => sum + holding.marketValue, 0);
  const cash = numberValue(state.cash);
  const totalMarketValue = stockValue + cash;
  const totalCost = holdings.reduce((sum, holding) => sum + holding.cost, 0);
  const totalProfit = holdings.reduce((sum, holding) => sum + holding.profit, 0);
  const totalReturn = totalCost > 0 ? (totalProfit / totalCost) * 100 : 0;
  const targetTotal = holdings.reduce((sum, holding) => sum + holding.target, 0);

  return { holdings, cash, totalMarketValue, stockValue, totalCost, totalProfit, totalReturn, targetTotal };
}

