function upsertTodaySnapshot(portfolio, force) {
  const now = new Date();
  const today = now.toISOString().slice(0, 10);
  const stamp = force ? now.toISOString().slice(0, 19).replace("T", " ") : today;
  const value = toTwd(portfolio.totalMarketValue);
  const cost = toTwd(portfolio.totalCost);
  const existing = performanceHistory.find((item) => item.date === today);

  if (force) {
    performanceHistory.push({ date: stamp, value, cost });
  } else if (existing) {
    return;
  } else {
    performanceHistory.push({ date: today, value, cost });
  }

  performanceHistory.sort((a, b) => a.date.localeCompare(b.date));
  saveHistory();
}

function renderPerformanceChart(format) {
  const canvas = els.performanceChart;
  if (!canvas) return;

  const ctx = canvas.getContext("2d");
  const width = canvas.width;
  const height = canvas.height;
  const dpr = window.devicePixelRatio || 1;
  const displayWidth = Math.max(600, Math.round(canvas.clientWidth * dpr));
  const displayHeight = Math.max(260, Math.round(canvas.clientHeight * dpr));

  if (canvas.width !== displayWidth || canvas.height !== displayHeight) {
    canvas.width = displayWidth;
    canvas.height = displayHeight;
  }

  const chartHistory = getFilteredPerformanceHistory();
  drawChart(ctx, canvas.width, canvas.height, format, chartHistory);

  if (chartHistory.length <= 1) {
    els.chartStatus.textContent = text.historyOnePoint;
  } else {
    const first = chartHistory[0].value;
    const last = chartHistory[chartHistory.length - 1].value;
    const returnRate = first > 0 ? ((last - first) / first) * 100 : 0;
    els.chartStatus.textContent = `${text.historyManyPoints} ${chartHistory.length} ${text.points}, ${format.format(first)} -> ${format.format(last)}, ${pct(returnRate)}.`;
  }
}

function getFilteredPerformanceHistory() {
  const history = performanceHistory.filter((item) => item && item.date && Number.isFinite(Number(item.value)));
  const range = state.chartRange || "all";
  if (range === "all" || history.length === 0) return history;

  const now = new Date();
  if (range === "ytd") {
    const year = String(now.getFullYear());
    return history.filter((item) => String(item.date).slice(0, 4) === year);
  }

  const months = numberValue(range);
  const cutoff = new Date(now);
  cutoff.setMonth(cutoff.getMonth() - months);
  const cutoffText = cutoff.toISOString().slice(0, 10);
  return history.filter((item) => String(item.date).slice(0, 10) >= cutoffText);
}

function drawChart(ctx, width, height, format, history) {
  ctx.clearRect(0, 0, width, height);
  ctx.fillStyle = "#ffffff";
  ctx.fillRect(0, 0, width, height);
  chartHoverState = null;

  const pad = {
    top: 26,
    right: 30,
    bottom: 48,
    left: 86,
  };

  const plotWidth = width - pad.left - pad.right;
  const plotHeight = height - pad.top - pad.bottom;
  const values = history.map((item) => Number(item.value));
  const minValue = Math.min(...values, 0);
  const maxValue = Math.max(...values, 1);
  const range = Math.max(maxValue - minValue, 1);

  ctx.strokeStyle = "#dfe4ea";
  ctx.lineWidth = 1;
  ctx.font = `${12 * (window.devicePixelRatio || 1)}px system-ui, sans-serif`;
  ctx.fillStyle = "#667282";

  for (let i = 0; i <= 4; i += 1) {
    const y = pad.top + (plotHeight / 4) * i;
    const value = maxValue - (range / 4) * i;
    ctx.beginPath();
    ctx.moveTo(pad.left, y);
    ctx.lineTo(width - pad.right, y);
    ctx.stroke();
    ctx.fillText(compactMoney(value, format), 12, y + 4);
  }

  if (history.length === 0) return;

  const firstValue = Number(history[0].value);
  const points = history.map((item, index) => {
    const x = history.length === 1 ? pad.left + plotWidth / 2 : pad.left + (plotWidth * index) / (history.length - 1);
    const y = pad.top + plotHeight - ((Number(item.value) - minValue) / range) * plotHeight;
    const performance = firstValue > 0 ? ((Number(item.value) - firstValue) / firstValue) * 100 : 0;
    return { x, y, item, performance };
  });
  chartHoverState = { points, format, dpr: window.devicePixelRatio || 1 };

  const gradient = ctx.createLinearGradient(0, pad.top, 0, height - pad.bottom);
  gradient.addColorStop(0, "rgba(15, 118, 110, 0.22)");
  gradient.addColorStop(1, "rgba(15, 118, 110, 0)");

  ctx.beginPath();
  points.forEach((point, index) => {
    if (index === 0) ctx.moveTo(point.x, point.y);
    else ctx.lineTo(point.x, point.y);
  });
  ctx.lineTo(points[points.length - 1].x, height - pad.bottom);
  ctx.lineTo(points[0].x, height - pad.bottom);
  ctx.closePath();
  ctx.fillStyle = gradient;
  ctx.fill();

  ctx.beginPath();
  points.forEach((point, index) => {
    if (index === 0) ctx.moveTo(point.x, point.y);
    else ctx.lineTo(point.x, point.y);
  });
  ctx.strokeStyle = "#0f766e";
  ctx.lineWidth = 3 * (window.devicePixelRatio || 1);
  ctx.stroke();

  ctx.fillStyle = "#0f766e";
  points.forEach((point) => {
    ctx.beginPath();
    ctx.arc(point.x, point.y, 4 * (window.devicePixelRatio || 1), 0, Math.PI * 2);
    ctx.fill();
  });

  ctx.fillStyle = "#667282";
  const first = points[0].item.date;
  const last = points[points.length - 1].item.date;
  ctx.fillText(first, pad.left, height - 18);
  ctx.textAlign = "right";
  ctx.fillText(last, width - pad.right, height - 18);
  ctx.textAlign = "left";
}

function compactMoney(value, format) {
  if (Math.abs(value) >= 1000000) return `${format.format(value / 1000000)}M`;
  if (Math.abs(value) >= 1000) return `${format.format(value / 1000)}K`;
  return format.format(value);
}

function showChartTooltip(event) {
  if (!chartHoverState || !els.chartTooltip) return;

  const rect = els.performanceChart.getBoundingClientRect();
  const x = (event.clientX - rect.left) * chartHoverState.dpr;
  const nearest = chartHoverState.points.reduce((best, point) => {
    const distance = Math.abs(point.x - x);
    return !best || distance < best.distance ? { point, distance } : best;
  }, null)?.point;

  if (!nearest) {
    els.chartTooltip.hidden = true;
    return;
  }

  const displayX = nearest.x / chartHoverState.dpr;
  const displayY = nearest.y / chartHoverState.dpr;
  els.chartTooltip.innerHTML = `
    <strong>${escapeHtml(nearest.item.date)}</strong><br>
    \u5e02\u503c ${escapeHtml(chartHoverState.format.format(Number(nearest.item.value)))}<br>
    \u7e3e\u6548 ${escapeHtml(pct(nearest.performance))}
  `;
  els.chartTooltip.hidden = false;
  els.chartTooltip.style.left = `${els.performanceChart.offsetLeft + Math.min(displayX + 18, rect.width - 200)}px`;
  els.chartTooltip.style.top = `${els.performanceChart.offsetTop + Math.max(10, displayY - 58)}px`;
}

function importPortfolioCsv(csvText) {
  const rows = parseCsv(csvText);
  const byLabel = new Map();

  for (const row of rows) {
    row.forEach((cell, labelIndex) => {
      const label = cell.trim();
      if (label && !isNumericLike(label) && !byLabel.has(label)) {
        byLabel.set(label, { row, labelIndex });
        byLabel.set(label.toLowerCase(), { row, labelIndex });
      }
    });
  }

  const symbols = byLabel.get("\u6a19\u7684");
  const targets = byLabel.get("\u8a2d\u5b9a\u6bd4\u4f8b");
  const shares = byLabel.get("\u6301\u6709\u80a1\u6578");
  const prices = byLabel.get("\u5e02\u503c");
  const totalValues = byLabel.get("\u7e3d\u50f9\u503c");
  const netValue = byLabel.get("\u76ee\u524d\u6de8\u503c");
  const importCurrencyInput = findLabeledCellValue(byLabel, ["Currency", "\u5e63\u5225", "\u8a08\u50f9\u5e63\u5225"]);
  const importFxRate = parseNumber(findLabeledCellValue(byLabel, ["TWD FX", "\u532f\u7387", "\u7f8e\u5143\u532f\u7387"])) || numberValue(state.fxRate) || numberValue(defaultState.fxRate);

  if (!symbols || !targets || !shares || !prices) {
    throw new Error(text.invalidCsv);
  }

  const start = symbols.labelIndex + 1;
  const importCurrency = importCurrencyInput ? normalizeCurrency(importCurrencyInput) : inferCsvPortfolioCurrency(symbols.row.slice(start), state.currency || defaultState.currency);
  const holdings = [];

  for (let index = start; index < symbols.row.length; index += 1) {
    const symbol = symbols.row[index]?.trim();
    if (!symbol) continue;

    const shareCount = parseNumber(shares.row[index]);
    if (shareCount === 0) continue;

    const market = inferMarket(symbol);
    const quoteCurrency = quoteCurrencyForMarket(market, importCurrency);
    const price = parseNumber(prices.row[index]);
    const totalCost = totalValues ? parseNumber(totalValues.row[index]) : shareCount * price;
    const avgCost = shareCount > 0 ? totalCost / shareCount : price;

    holdings.push({
      symbol,
      name: symbol,
      market,
      shares: shareCount,
      avgCost,
      price,
      target: parsePercent(targets.row[index]),
      quoteCurrency,
    });
  }

  if (holdings.length === 0) {
    throw new Error(text.invalidCsv);
  }

  const importedTotal = netValue ? parseNumber(netValue.row[netValue.labelIndex + 1]) : 0;
  const stockValue = holdings.reduce((sum, holding) => {
    const value = holding.shares * holding.price;
    return sum + convertValueBetweenCurrencies(value, holding.quoteCurrency, importCurrency, importFxRate);
  }, 0);
  const cash = importedTotal > stockValue ? importedTotal - stockValue : 0;

  return {
    ...structuredClone(defaultState),
    cash,
    currency: importCurrency,
    fxRate: importFxRate,
    holdings,
  };
}

function findLabeledCellValue(byLabel, labels) {
  for (const label of labels) {
    const match = byLabel.get(label) || byLabel.get(label.toLowerCase()) || byLabel.get(label.toUpperCase());
    const value = match?.row[match.labelIndex + 1]?.trim();
    if (value) return value;
  }
  return "";
}

function normalizeCurrency(value) {
  const currency = String(value || defaultState.currency).trim().toUpperCase();
  if (["TWD", "\u53f0\u5e63", "\u65b0\u53f0\u5e63"].includes(currency)) return "TWD";
  if (["USD", "\u7f8e\u5143", "\u7f8e\u91d1"].includes(currency)) return "USD";
  if (["JPY", "\u65e5\u5713", "\u65e5\u5e63"].includes(currency)) return "JPY";
  return currency || defaultState.currency;
}

function inferCsvPortfolioCurrency(symbols, fallbackCurrency) {
  return symbols.some((symbol) => inferMarket(symbol) === "TW") ? "TWD" : normalizeCurrency(fallbackCurrency);
}

function parseCsv(csvText) {
  const rows = [];
  let row = [];
  let cell = "";
  let quoted = false;

  for (let index = 0; index < csvText.length; index += 1) {
    const char = csvText[index];
    const next = csvText[index + 1];

    if (char === '"' && quoted && next === '"') {
      cell += '"';
      index += 1;
    } else if (char === '"') {
      quoted = !quoted;
    } else if (char === "," && !quoted) {
      row.push(cell);
      cell = "";
    } else if ((char === "\n" || char === "\r") && !quoted) {
      if (char === "\r" && next === "\n") index += 1;
      row.push(cell);
      rows.push(row);
      row = [];
      cell = "";
    } else {
      cell += char;
    }
  }

  if (cell || row.length > 0) {
    row.push(cell);
    rows.push(row);
  }

  return rows;
}

function parseNumber(value) {
  const cleaned = String(value ?? "").replaceAll(",", "").replace("%", "").trim();
  const parsed = Number(cleaned);
  return Number.isFinite(parsed) ? parsed : 0;
}

function parsePercent(value) {
  return parseNumber(value);
}

function isNumericLike(value) {
  const trimmed = String(value ?? "").trim();
  return trimmed === "" || /^-?\d+(\.\d+)?%?$/.test(trimmed);
}

