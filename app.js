function renderSummary(portfolio, format) {
  const targetDiff = portfolio.targetTotal - 100;
  const targetClass = Math.abs(targetDiff) < 0.01 ? "profit" : "watch";
  els.allocationStatus.innerHTML = `${text.allocationTotal} <strong class="${targetClass}">${pct(portfolio.targetTotal)}</strong>, ${text.cash} ${format.format(portfolio.cash)}.`;

  const threshold = numberValue(state.threshold);
  const actionable = portfolio.holdings
    .map((holding) => {
      const currentPct = portfolio.totalMarketValue > 0 ? (holding.marketValue / portfolio.totalMarketValue) * 100 : 0;
      const gapPct = holding.target - currentPct;
      const targetValue = portfolio.totalMarketValue * (holding.target / 100);
      const gapValue = targetValue - holding.marketValue;
      const portfolioPrice = convertToPortfolioCurrency(holding.price, holding.quoteCurrency);
      const suggestedShares = portfolioPrice > 0 ? Math.trunc(gapValue / portfolioPrice) : 0;

      return { holding, currentPct, gapPct, gapValue, suggestedShares };
    })
    .filter((item) => Math.abs(item.gapPct) >= threshold && item.suggestedShares !== 0)
    .sort((a, b) => Math.abs(b.gapPct) - Math.abs(a.gapPct));

  els.rebalanceList.innerHTML = "";

  if (actionable.length === 0) {
    const empty = document.createElement("div");
    empty.className = "rebalance-item";
    empty.innerHTML = `<strong>${text.noAdjust}</strong><span>${text.withinThreshold}</span>`;
    els.rebalanceList.append(empty);
    return;
  }

  actionable.forEach((item) => {
    const card = document.createElement("article");
    card.className = "rebalance-item";
    card.innerHTML = `
      <strong>${escapeHtml(item.holding.symbol || item.holding.name || text.unnamed)}</strong>
      <span>${text.current} ${pct(item.currentPct)}, ${text.target} ${pct(item.holding.target)}, ${text.gap} ${signedMoney(item.gapValue, format)}.</span>
      <span class="${item.suggestedShares > 0 ? "profit" : "loss"}">${formatSuggestion(item.suggestedShares)}</span>
    `;
    els.rebalanceList.append(card);
  });
}

